# Funciones de una calculadora
def sumar(a, b):
    """
    Suma dos variables: a + b
    """
    return a+b


def restar(a, b):
    """
    Resta dos variables: a-b
    """
    return a-b


def multiplicar(a, b):
    """
    Multiplica dos variables: a*b
    """
    return a*b


def dividir(a, b):
    """
    Divide dos variables: a/b
    """
    return a/b
